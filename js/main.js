function wavy(){
  document.querySelector('body').style.backgroundImage = 'url(./img/waves.webp)'
  document.querySelector('body').style.backgroundRepeat = 'no-repeat'
  document.querySelector('body').style.backgroundSize = '1000px 900px'
  document.querySelector('body').style.color = 'white'
}

